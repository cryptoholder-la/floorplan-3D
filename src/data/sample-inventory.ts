export const getMaterialById = (id: string): Material | undefined => {
  return sampleMaterials.find(material => material.id === id);
};

export const getInventoryItemById = (id: string): InventoryItem | undefined => {
  return sampleInventoryItems.find(item => item.id === id);
};

export const getInventoryItemsByType = (type: InventoryItem['type']): InventoryItem[] => {
  return sampleInventoryItems.filter(item => item.type === type);
};

export const getLowStockItems = (): InventoryItem[] => {
  return sampleInventoryItems.filter(item => 
    item.minQuantity && item.quantity <= item.minQuantity
  );
};

export const getOutOfStockItems = (): InventoryItem[] => {
  return sampleInventoryItems.filter(item => !item.inStock || item.quantity === 0);
};
